package Model;

public class MetricsetBean {
    private String name;
    private String module;
    private int rtt;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getModule() {
        return module;
    }
    public void setModule(String module) {
        this.module = module;
    }
    public int getRtt() {
        return rtt;
    }
    public void setRtt(int rtt) {
        this.rtt = rtt;
    }
}
